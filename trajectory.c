void character(int x, int y, uint32_t color, char c) {
    if (c < 0 || c > 127) return; // unsupported char
    for (int row = 0; row < 8; row++) {
        uint8_t bits = font8x8_basic[(int)c][row];
        for (int col = 0; col < 8; col++) {
            if (bits & (1 << col)) {
                int px = x + col;
                int py = y + row;
                if (px >= 0 && px < WIDTH && py >= 0 && py < HEIGHT) {
                    framebuffer32[py * WIDTH + px] = color;
                }
            }
        }
    }
}
void print(const char *str, uint32_t color, int x, int y) {
     int cursor_x = x, cursor_y = y;
    while (*str) {
        if (*str == '\n') {
            cursor_x = 0;
            cursor_y += 8;
            if (cursor_y + 8 > HEIGHT) {
                cursor_y = 0; // simple wrap
            }
        } else {
            character(cursor_x, cursor_y, color, *str);
            cursor_x += 8;
            if (cursor_x + 8 > WIDTH) {
                cursor_x = 0;
                cursor_y += 8;
                if (cursor_y + 8 > HEIGHT) {
                    cursor_y = 0; // simple wrap
                }
            }
        }
        str++;
    }
}


int main() {
    Display *dpy = XOpenDisplay(NULL);
    if (!dpy) return 1;

    int screen = DefaultScreen(dpy);
    Window win = XCreateSimpleWindow(dpy, RootWindow(dpy, screen),
                                     0, 0, WIDTH, HEIGHT, 1,
                                     BlackPixel(dpy, screen),
                                     WhitePixel(dpy, screen));
    XSelectInput(dpy, win, ExposureMask | KeyPressMask);
    XMapWindow(dpy, win);
    GC gc = DefaultGC(dpy, screen);

    // Create XImage with its own allocated pixel memory (will free with XDestroyImage)
    XImage *ximage = XCreateImage(dpy, DefaultVisual(dpy, screen),
                                  24, ZPixmap, 0, malloc(WIDTH * HEIGHT * 4),
                                  WIDTH, HEIGHT, 32, 0);
    if (!ximage || !ximage->data) return 1;
    //int thingy = 1/0;
    float angle = 45.5f;
    float velocity = 60.0f;
    float thing = 0.5f;
    while (1) {
        XEvent e;
        while (XPending(dpy)) {
            XNextEvent(dpy, &e);
            if (e.type == KeyPress) goto cleanup;
        }
        for (int y = 0; y < HEIGHT; y++) {
            for (int x = 0; x < WIDTH; x++) {
                framebuffer32[y * WIDTH + x] = 0;
            }
        }
        // draw something into framebuffer32 here
        // we will draw an trajectory, based on launch angle and velocity
        angle += thing;
        //trajectory(360,360,90+angle,60.0f,0xFFFFFF);
        
        if (angle > 89.0f) thing = -0.5f;
        if (angle < 1.0f) thing = 0.5f;
        trajectory(30,240,angle, 60.0f, 0xFFFFFF); // Red trajectory
        print("Angle change (1 to 89)", 0xffffff, 30, 100);
        velocity += thing;
        trajectory(30,360,45.5f, velocity, 0x00FF00); // Green trajectory
        print("Velocity change (60 to like 105)", 0xffffff, 30, 400);
        
        // copy converted pixels into XImage memory and blit
        //convert_to_ximage();
        memcpy(ximage->data, framebuffer32, WIDTH * HEIGHT * 4);
        XPutImage(dpy, win, gc, ximage, 0, 0, 0, 0, WIDTH, HEIGHT);

        usleep(50000); // ~60 FPS
    }

cleanup:
    XDestroyImage(ximage);
    XCloseDisplay(dpy);
    return 0;
}
